          spriteOrigin: new kakao.maps.Point(46, order * 36), // Ïä§ÌîÑÎùºÏù¥Ìä∏ Ïù¥ÎØ∏ÏßÄ Ï§ë ÏÇ¨Ïö©Ìï† ÏòÅÏó≠Ïùò Ï¢åÏÉÅÎã® Ï¢åÌëú
          offset: new kakao.maps.Point(11, 28), // ÎßàÏª§ Ï¢åÌëúÏóê ÏùºÏπòÏãúÌÇ¨ Ïù¥ÎØ∏ÏßÄ ÎÇ¥ÏóêÏÑúÏùò Ï¢åÌëú
        },
        markerImage = new kakao.maps.MarkerImage(imageSrc, imageSize, imgOptions),
        marker = new kakao.maps.Marker({
          position: position, // ÎßàÏª§Ïùò ÏúÑÏπò
          image: markerImage,
        });

      // console.log('this.map : ',this.map);
      marker.setMap(this.map); // ÏßÄÎèÑ ÏúÑÏóê ÎßàÏª§Î•º ÌëúÏ∂úÌï©ÎãàÎã§
      this.markers.push(marker); // Î∞∞Ïó¥Ïóê ÏÉùÏÑ±Îêú ÎßàÏª§Î•º Ï∂îÍ∞ÄÌï©ÎãàÎã§

      return marker;
    },

    // ÏßÄÎèÑ ÏúÑÏóê ÌëúÏãúÎêòÍ≥† ÏûàÎäî ÎßàÏª§Î•º Î™®Îëê Ï†úÍ±∞Ìï©ÎãàÎã§
    removeMarker() {
      for (var i = 0; i < this.markers.length; i++) {
        this.markers[i].setMap(null);
      }
      this.markers = [];
    },

    // ÌÅ¥Î¶≠Ìïú ÎßàÏª§Ïóê ÎåÄÌïú Ïû•ÏÜå ÏÉÅÏÑ∏Ï†ïÎ≥¥Î•º Ïª§Ïä§ÌÖÄ Ïò§Î≤ÑÎ†àÏù¥Î°ú ÌëúÏãúÌïòÎäî Ìï®ÏàòÏûÖÎãàÎã§
    displayPlaceInfo(place) {
      var content =
        '<div class="placeinfo">' +
        '   <a class="title" href="' +
        place.place_url +
        '" target="_blank" title="' +
        place.place_name +
        '">' +
        place.place_name +
        '</a>';

      if (place.road_address_name) {
        content +=
          '    <span title="' +
          place.road_address_name +
          '">' +
          place.road_address_name +
          '</span>' +
          '  <span class="jibun" title="' +
          place.address_name +
          '">(ÏßÄÎ≤à : ' +
          place.address_name +
          ')</span>';
      } else {
        content += '    <span title="' + place.address_name + '">' + place.address_name + '</span>';
      }

      content +=
        '    <span class="tel">' + place.phone + '</span>' + '</div>' + '<div class="after"></div>';

      this.contentNode.innerHTML = content;
      this.placeOverlay.setPosition(new kakao.maps.LatLng(place.y, place.x));
      this.placeOverlay.setMap(this.map);
    },

    // Ïπ¥ÌÖåÍ≥†Î¶¨Î•º ÌÅ¥Î¶≠ÌñàÏùÑ Îïå Ìò∏Ï∂úÎêòÎäî Ìï®ÏàòÏûÖÎãàÎã§
    onClickCategory(catrgoryId) {
      var id = catrgoryId,
        className = document.getElementById(id).className;

      this.placeOverlay.setMap(null);

      if (className === 'on') {
        this.currCategory = '';
        this.changeCategoryClass();
        this.removeMarker();
      } else {
        this.currCategory = id;
        this.changeCategoryClass(id);
        this.searchPlaces();
      }
    },

    // ÌÅ¥Î¶≠Îêú Ïπ¥ÌÖåÍ≥†Î¶¨ÏóêÎßå ÌÅ¥Î¶≠Îêú Ïä§ÌÉÄÏùºÏùÑ Ï†ÅÏö©ÌïòÎäî Ìï®ÏàòÏûÖÎãàÎã§
    changeCategoryClass(categoryId) {
      var category = document.getElementById('category'),
        children = category.children,
        i;

      for (i = 0; i < children.length; i++) {
        children[i].className = '';
      }

      if (categoryId) {
        for (var j = 0; j < children.length; j++) {
          if (children[j].getAttribute('id') === categoryId) {
            children[j].className = 'on';
          }
        }
      }
    },
  },
};
</script>

